# Append --> add value in last position
a = [1,2,3]
a.append(4)
print(a)

# Insert --> add value in anywheere
a = [1,2,3,4]
a.insert(2,10) # 2 is position(index_value) and 10 is value that adding in list
print(a)

# pop --> remove given index value from the table
a.pop(2) # 2 is index_value
print(a) 

# Remove --> remove given value from the table
b = [10,20,30,10,20]
b.remove(10) # 10 is value
print(b)